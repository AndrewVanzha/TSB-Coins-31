<p>
	 Рекомендации по эксплуатации и особенности хранения монет.
</p>
<p>
	 Монеты рекомендуется хранить внутри пластиковой капсулы — это обезопасит монету от воздействия неблагоприятной внешней среды и от механического воздействия.
</p>
<p>
	 Чистка монет возможна только специалистами, обладающими соответствующей квалификацией. Для этих целей покупателю рекомендуется обращаться в специализированные компании.
</p>
<p>
	 Монеты, исполненные в обычном качестве — АЦ, можно изымать из капсулы, но при этом рекомендуется пользоваться специальными ювелирными перчатками и ювелирными салфетками в целях смягчения поверхности.&nbsp; Ни в коем случае нельзя тереть монеты ни перчатками, ни салфетками.
</p>
<p>
	 Монеты, исполненных в качестве пруф, не рекомендуется изымать из капсулы. В том случае, если под механическим воздействием монета оказалась без капсулы, ни в коем случае нельзя брать монету без специальных ювелирных перчаток. Монету в качестве пруф, оказавшуюся без капсулы, можно брать только в перчатках за кант монеты.
