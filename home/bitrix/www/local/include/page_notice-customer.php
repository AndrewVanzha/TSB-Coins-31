<p>
	 Что нужно знать…
</p>
<p>
</p>
<p>
	 1. Лицевая сторона монеты (аверс) несет изображение официальной символики (государственного герба, эмблемы банка эмитента, различных художественных символов), номинал монеты, год выпуска. Кроме того, на&nbsp;аверс может наноситься обозначение драгоценного металла по&nbsp;периодической системе Д.&nbsp;И.&nbsp;Менделеева, проба сплава, масса химически чистого драгоценного металла в&nbsp;монете в&nbsp;граммах и&nbsp;товарный знак монетного двора-изготовителя.
</p>
<p>
	 2. На&nbsp;оборотной стороне (реверсе) расположены различные изображения, портреты людей и&nbsp;мотивы, относящиеся к&nbsp;теме, которой посвящена монета, или иная символика.
</p>
<p>
	 3. Встречаются исключения, когда номинал монеты расположен на&nbsp;реверсе монеты.
</p>
<p>
	 4. Край диска монеты со&nbsp;стороны аверса и&nbsp;реверса обрамляет кант, который в большинстве случаев выступает над рельефным изображением обеих сторон монеты и&nbsp;в&nbsp;плане имеет форму сплошного узкого кольца. На некоторых монетах данный край не выступает над рельефным изображением.
</p>
<p>
	 5. Боковая поверхность монеты называется гуртом.
</p>
<p>
	 6. Рисунки и надписи, наносимые на аверсы и реверсы монет, исполняются рельефно, то есть выступают над полем — плоскими участками диска монеты, лишенными каких-либо изображений и надписей.
</p>
<p>
	 7. Совокупность всех встречающихся на&nbsp;монете (на&nbsp;аверсе, реверсе и&nbsp;гурте) буквенных и&nbsp;цифровых надписей называется легендой.
</p>
<p>
	 8. Лигатурной массой монеты называют ее&nbsp;общую массу в&nbsp;граммах.
</p>
<p>
	 9. Содержание драгоценного металла в&nbsp;монете отражает абсолютное значение количества химически чистого драгоценного металла в&nbsp;монете в&nbsp;граммах.
</p>
<p>
	 10. Проба монеты показывает соотношение между количеством химически чистого драгоценного металла в&nbsp;монете и&nbsp;ее&nbsp;общей (лигатурной) массой. Выражается количеством весовых единиц химически чистого драгоценного металла в&nbsp;1000 весовых единиц лигатурной массы монеты.
</p>
<p>
	 11. По&nbsp;особенностям технологического процесса чеканки монеты делятся на&nbsp;две основные группы — исполненные в&nbsp;качестве «пруф» (или «пруф-лайк») и исполненные в&nbsp;обычном («А/Ц») или улучшенном («Б/А») качестве. Монеты качества пруф изготавливаются методом, позволяющим получить на&nbsp;поверхности монеты чистое, зеркальное поле и&nbsp;матовое рельефное изображение и&nbsp;надписи, рисунки их&nbsp;реверсов зачастую сложные по&nbsp;композиции, многоплановые, а надписи указывают на&nbsp;тематику, которой посвящен выпуск монет.
</p>
<p>
	 12. В&nbsp;последнее время монетные дворы освоили ряд технологий изготовления памятных монет в специальном исполнении с применением новых приемов обработки поверхности монет, таких как матирование лазерным инструментом, оксидирование, нанесение цветных фрагментов, декорирование кристаллическими вставками, reverse frosted, при которой на&nbsp;поверхности монеты формируется шелковисто-матовое поле, а&nbsp;отдельные детали рельефного рисунка исполняются в&nbsp;технике пруф и другие.
</p>
<p>
	 13. Монеты обычного качества (юбилейные монеты)&nbsp;— это продукция традиционного высокоавтоматизированного чеканного производства: курсовые и&nbsp;разменные монеты (обращающиеся по&nbsp;номиналам в&nbsp;качестве реальных платежных средств), а&nbsp;также монеты из&nbsp;драгоценных металлов инвестиционного назначения, тиражи которых могут достигать нескольких миллионов штук. На&nbsp;монетах этого вида нет зеркальных поверхностей, а поля, рисунки и&nbsp;надписи имеют одинаковую поверхностную фактуру, рельефы не&nbsp;контрастируют с&nbsp;полями. Рисунки на&nbsp;реверсах таких монет зачастую несложные, одноплановые. Главное требование, предъявляемое к&nbsp;таким монетам&nbsp;— соответствие заявленным параметрам по&nbsp;массе химически чистого драгоценного металла, что, в&nbsp;свою очередь, при их&nbsp;обращении обеспечивается отсутствием механических повреждений.
</p>
<p>
	 Монеты улучшенного качества отличаются от&nbsp;монет обычного качества главным образом тем, что они не&nbsp;имеют видимых без использования специальных технических средств мелких царапин, что достигается за&nbsp;счет применения автоматических станков и&nbsp;оборудования, снабженных устройствами, предотвращающими соприкосновение готовых монет&nbsp;и, тем самым, устраняющими возможность образования на&nbsp;их поверхности царапин.
</p>
