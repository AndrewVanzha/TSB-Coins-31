<p>
	 Компоновка и упаковка монет
</p>
<p>
	 Практически все монеты, продаваемые Банком (за исключением юбилейных монет), находятся в специальных пластиковых капсулах. Пластиковая капсула защищает монету от неблагоприятного воздействия внешней среды и от неблагоприятного механического воздействия. Данная пластиковая капсула не является герметической упаковкой и неделимой частью монеты. Пластиковая упаковка подвержена открытию.&nbsp; Банк в целях сохранения идеального состояния качества монеты не рекомендует открывать пластиковую капсулу. Капсула монеты не рассчитана на сильное механическое воздействие и может быть испорчена при механическом воздействии, что может повлечь порчу монеты.
</p>
<p>
	 В комплектацию большинства монет входит футляр для хранения монеты. Внешний вид, материал, из которого изготовлен футляр, качество, размер футляра не являются типичными для всех монет. Футляр также не является неразрывной частью монеты в капсуле. Монета в капсуле может быть изъята из футляра.
</p>
<p>
	 Некоторые монеты поставляются в специальных блистерах. В большинстве случаев данные блистеры до приемки товара покупателем являются неразрывной частью монеты. Монеты с блистером, не купленные покупателем, находятся всегда внутри блистера. Те монеты, которые по своему назначению эксплуатации подлежат изъятию из блистера, после приемки товара покупателем могут быть изъяты из блистера и использованы в соответствии с назначением. Монеты, для которых блистер является неразрывной упаковкой рекомендуется всегда хранить в блистере.
</p>
<p>
	 В комплектацию некоторых монет входят дополнительные аксессуары, такие как шнурок, цепочка и т. д. Их использование возможно только строго по назначению, гарантия на дополнительные аксессуары не распространяется.
</p>
<p>
	 В целях безопасной доставки монет до покупателя, Банк оставляет за собой право использования дополнительных защитных предметов, таких как пенопласт, полиэтиленовая пленка и т. д., которые бы гарантировали сохранность монеты, капсулы, футляра и иного дополнительного аксессуара.
